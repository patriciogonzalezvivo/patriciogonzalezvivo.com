			<p>Within our contemporary culture of play, we see the coexistence of our traditional games inheritance with cutting-edge technology. This rich encounter between the past and the “future” raises new and exciting questions about how we play and what kind of role playing has in our society.</p> 

			<p>The act of play can be seen through different theories and perspectives. Post-Freudian psychoanalysts see it not just as a potential medium for the unconscious to manifest but as a space with a unique nature between reality and fantasy where errors can be made without consequence and rules may be blend according to the imagination. This is an essential ability in order to think and explore different points of view and ways of looking at the world, which constitute the basic framework of democratic society. As such, for this project we focus on how we can see through the act of playing to discover cultural and social patterns and, at the same time, create a playground to explore new dynamics and ways of thinking.</p>

			<p>From this inquiry, “Mesa Del Tiempo” was originally designed for the <a href="http://museodeljuguetesi.org.ar/" target="_blank"> Toy Museum of San Isidro </a>(Buenos Aires, Argentina) to develop new configurations of traditional games using cutting-edge technology. Each one of the different installations (Shadows, Kaleidoscope, Simon, and Oca, among others) proposes new models of play.</p>
		</article>
		
		<article>
			<p><strong>Kaleidoscope</strong> is based on the popular and antique optical toy which it recreates and transforms in order to allow multiple users to interact with each other in a playful aesthetic dialog.</p>
			<p>It invites exploration of different compositions of textures and colors by using simple wooden blocks of different shapes. When each one of these geometric elements touches the surface of the table, a texture is virtually attached and then projected over it. There is a rotating triangle area that mirrors everything inside of it. Just as with a real kaleidoscope, the images are repeated all around the table.</p>
			<p>The Kaleidoscope installation was designed to put this traditional optic toy on the contemporary playing field, updating it to allow multiple simultaneous players, and thereby revealing the multiple views and possibilities that happen through collaboration.</p>
